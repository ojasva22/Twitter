package com.bhtwitter.twitter.exception;

public class FollowerException extends RuntimeException {

	public FollowerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public FollowerException(String arg0) {
		super(arg0);
		
	}

	public FollowerException(Throwable arg0) {
		super(arg0);
		
	}
	

}
